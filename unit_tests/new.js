function add(x, y) {
    return x + y;
}

function multiply(x, y) {
    return x * y;
}

function divide(x, y) {
    return x / y;
}

function subtract(x, y) {
    return x - y;
}

module.exports = {};
module.exports.add = add;
module.exports.multiply = multiply;
module.exports.divide = divide;
module.exports.subtract = subtract;